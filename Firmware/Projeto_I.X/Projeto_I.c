/* ========================================================================= */
/* --- Function Main --- */
void main(void) {
    TRISB = 0;
    LATB = 0 ;

    
/* ========================================================================= */
/* --- Loop --- */
    while(1){
        LATB = 0b00110000;
        __delay_ms(7000);
        LATB = 0b01010000;
        __delay_ms(1000);
        LATB = 0b10000100;
        __delay_ms(7000);
        LATB = 0b10001000;
        __delay_ms(1000);
    }
}


